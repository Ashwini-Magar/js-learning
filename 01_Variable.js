
console.log("WAP to swap variable values using third variable");

var n1 = 200; // 200

var n2 = 400; // 100

console.log("======== Before Swap ==========");

console.log("n1:", n1, " n2:", n2);

 

console.log("======== After Swap ==========");

var temp = n1;

n1 = n2;

n2 = temp;

console.log("n1:", n1, " n2:", n2);